# Casting: string to int using int(x)
x = "5"
y = 10

print(type(y))

answer = int(x) + y # string to int
print(answer)

# Casting: string to float using float(x)
x = "5"
y = 10

print(type(y))

answer = float(x) + y # string to float
print(answer)

# Casting: int to str using str(x)
x = "5"
y = 10

print(type(y))

answer = str(y) + x # int to str
print(answer)

print('type of answer', type(answer))

answer = float(answer)
print('type of answer3', type(answer))


